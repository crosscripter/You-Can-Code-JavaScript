// Arithmetic Expressions


// Basic Arithmetic Operations

// Define two numbers
const num1 = 10;
const num2 = 3;

// Addition
const sum = num1 + num2; 
console.log(`Sum: ${sum}`); // Output: Sum: 13

// Subtraction
const difference = num1 - num2; 
console.log(`Difference: ${difference}`); // Output: Difference: 7

// Multiplication
const product = num1 * num2; 
console.log(`Product: ${product}`); // Output: Product: 30

// Division
const quotient = num1 / num2; 
console.log(`Quotient: ${quotient}`); // Output: Quotient: 3.333...

// Modulus
const remainder = num1 % num2; 
console.log(`Remainder: ${remainder}`); // Output: Remainder: 1


// Combining Arithmetic and Variables

// Monthly salary in USD
const baseSalary = 3000;

// Calculate yearly salary with bonus
const yearlyBonus = 5000;
const yearlySalary = (baseSalary * 12) + yearlyBonus;
console.log(`Yearly Salary: ${yearlySalary}`); // Output: Yearly Salary: 41000


// Complex Arithmetic Expression

// Rectangle dimensions in meters
const length = 5;
const width = 3;

// Calculate perimeter and area
const perimeter = 2 * (length + width);
const area = length * width;

console.log(`Perimeter: ${perimeter} meters`); // Output: Perimeter: 16 meters
console.log(`Area: ${area} square meters`);   // Output: Area: 15 square meters
