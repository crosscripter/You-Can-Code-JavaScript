// Escaping Quotes and Special Characters

// Escapes (\) 
let quoteExample = 'She said, "JavaScript is awesome!"';
let multilineString = 'This is a line.\nAnd this is another line.';
console.log(multilineString);

// Special Escape Codes 

// Newline (\n)
let multiLine = 'This is line one.\nThis is line two.';
console.log(multiLine);

// Tab (\t)
let tabbed = 'Column 1\tColumn 2\tColumn 3';
console.log(tabbed);

// Backslash (\\)
let path = 'C:\\Program Files\\JavaScript';
console.log(path);

// Quotes (\' or \")
let quote = "He said, \"Hello!\"";
console.log(quote);

// Carriage Return (\r)
let carriageReturn = 'Hello\rWorld';
console.log(carriageReturn);

// Null Character (\0)
let nullChar = 'Hello\0World';
console.log(nullChar);

// Unicode (\uXXXX)
let smiley = "\u263A"; 
console.log(smiley);
