/* Comparison Operators
==========================================================
Operator    Description             Example         Result
==          Equal to                5 == "5"        true
===         Strict equal            5 === "5"       false
!=          Not equal               5 != 4          true
<           Less than               3 < 5           true
<=          Less than or equal      3 <= 3          true
>           Greater than            4 > 3           true
>=          Greater than or equal   4 >= 4          true
==========================================================
*/

// Equal to (==)
console.log(5 == '5');

// Strict Equal (===)
console.log(5 === '5');

// Not Equal (!=)
console.log(0 != 5);

// Less Than (<)
console.log(1 < 2);

// Less Than or Equal
console.log(2 <= 2);

// Greater Than (>)
console.log(5 > 4);

// Greater Than or Equal (>=)
console.log(4 >= 4);
